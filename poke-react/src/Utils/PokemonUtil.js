
const getPokemonTypeColor = (type) => {

    switch(type) {
        case 'normal':   return '';
        case 'flying':   return '';
        case 'poison':   return '';
        case 'bug':      return '';
        case 'fire':     return '';
        case 'ice':      return '';
        case 'ground':   return '';
        case 'rock':     return '';
        case 'water':    return '';
        case 'electric': return '';
        case 'dragon':   return '';
        case 'steel':    return '';
    }

}

export default getPokemonTypeColor;